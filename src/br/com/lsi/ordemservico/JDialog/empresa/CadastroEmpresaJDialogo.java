    private void JButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonSalvarActionPerformed

        String nomeFantasia = jTextNomeFantasia.getText();
        String CNPJ = jFormateCNPJ.getText();

        String rua = jTextRua.getText();
        String bairro = jTextBairro.getText();
        String uf = (String) jComboBoxUF.getSelectedItem();
        String cidade = jTextCidade.getText();
        String cep = jFormateCEP.getText();

        empresa.setNomeFantasia(nomeFantasia);
        empresa.setCNPJ(CNPJ);

        Endereco endereco = new Endereco();

        endereco.setRua(rua);
        endereco.setBairro(bairro);
        endereco.setUf(uf);
        endereco.setCidade(cidade);
        endereco.setCep(cep);

        empresa.setEnderecoEmpresa(endereco);

        try {
            empresaFacade.InserirEmpresa(empresa);
            //JOptionPane.showMessageDialog(null,"Cliente SALVO com sucesso!");
            int resp = JOptionPane.showConfirmDialog(null, "Salvar cadastro da empresa?", "Cadastramento", JOptionPane.YES_NO_OPTION);
            if (resp == 0) {
                dispose();
            }
        } catch (Exception ex) {
            Logger.getLogger(CadastroEmpresaJDialogo.class.getName()).log(Level.SEVERE, null, ex);
        }

        jTextNomeFantasia.setText("");
        jFormateCNPJ.setText("");

        jComboBoxUF.setSelectedItem("");
        jTextRua.setText("");
        jTextBairro.setText("");
        jComboBoxUF.setSelectedItem("");
        jFormateCEP.setText(uf);

    }//GEN-LAST:event_JButtonSalvarActionPerformed

