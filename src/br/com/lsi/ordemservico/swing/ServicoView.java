    public void preencherTabela () throws DAOException{
        
        tabela.setRowHeight(25);
        modelo.setNumRows(0);
        
        List<Servico> lista = new ArrayList<>();
        
        lista = fachada.getAllServicos();
        
        try {

            if (lista.size() > 0) {
                for (Servico servico : lista) {
                    modelo.addRow(new Object[]{servico.getId(),servico.getNome(),servico.getPreco(),servico.getDescricao()});
                }
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERRO AO LISTAR");
        }
        
    }

