    private void btEdit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEdit1ActionPerformed
        int linhaSelecionada = tabela.getSelectedRow();//pegando linha selecionada
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Produto não selecionado");
        }
        String id = (tabela.getValueAt(linhaSelecionada, 0).toString());
        try {
            ServicoEdit serEdit = new ServicoEdit(null, true);
            serEdit.inserirServico(fachada.getByIdServicos(Long.parseLong(id)));
            Servico ser = new Servico();
            ser = fachada.getByIdServicos(Long.parseLong(id));
            serEdit.id = ser.getId();
            serEdit.setVisible(true);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btEdit1ActionPerformed

    private void btExcluir1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluir1ActionPerformed
        int linhaSelecionada = tabela.getSelectedRow();//pegando linha selecionada
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Serviço não selecionado");
        }
        String id = (tabela.getValueAt(linhaSelecionada, 0).toString());
        int x = JOptionPane.showConfirmDialog(null, "Deseja Deletar");
        if (x == 0) {
            try {
                fachada.deletarServicos(fachada.getByIdServicos(Long.parseLong(id)));
                JOptionPane.showMessageDialog(null, "Excluido com Sucesso");
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showConfirmDialog(null, "Erro ao Excluir");
            }
        }

        try {
            preencherTabela();
        } catch (DAOException ex) {
            Logger.getLogger(ProdutoView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btExcluir1ActionPerformed

